#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	i;
	char	*tmp;

	i = -1;
	while (++i <= ft_strlen(s))
		if (s[ft_strlen(s) - i] == c)
			return (tmp = (char *)(s + ft_strlen(s) - i), tmp);
	return (NULL);
}
