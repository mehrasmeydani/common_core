#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	int		i;
	char	*tmp1;
	char	*tmp2;

	if (!src ||!dest)
		return (NULL);
	tmp1 = dest;
	tmp2 = src;
	i = -1;
	while (++i < n)
		tmp1[i] = tmp2[i];
	return (dest);
}
