#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;
	t_uc	*tmp;

	tmp = (t_uc *)s;
	i = -1;
	while (++i < n)
		tmp[i] = c;
	return (s);
}
