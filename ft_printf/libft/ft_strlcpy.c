#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dsize)
{
	size_t	i;

	if (dsize <= 0)
		return (ft_strlen(src));
	i = -1;
	while (src[++i] != '\0' && i < dsize - 1)
		dst[i] = src[i];
	dst[i] = 0;
	return (ft_strlen(src));
}
