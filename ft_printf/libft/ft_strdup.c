#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*out;

	out = (char *)ft_calloc(ft_strlen(s) + 1, sizeof(char));
	if (!out)
		return (NULL);
	ft_strlcpy(out, s, ft_strlen(s) + 1);
	return (out);
}
