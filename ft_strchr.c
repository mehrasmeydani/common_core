#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*tmp;

	i = -1;
	while (s[++i])
		if (s[i] == c)
			return (tmp = (char *)(s + i), tmp);
	if (s[i] == c)
		return (tmp = (char *)(s + i), tmp);
	return (NULL);
}
